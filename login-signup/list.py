# Create an empty list
my_list = []

# Append elements to the list
my_list.append(1)
my_list.append(2)
my_list.append(3)

# Insert an element at a specific index
my_list.insert(1, 4)

# Remove an element from the list
my_list.remove(2)

# Clear all elements from the list
my_list.clear()

# Print the final list
print(my_list)